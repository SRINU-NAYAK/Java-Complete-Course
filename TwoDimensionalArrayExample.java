package Arrays;

public class TwoDimensionalArrayExample {
    public static void main(String[] args) {
        int arr[][]={{10,20,30},{40,50,60}};

        for(int i=0;i<=1;i++)
        {
            for(int j=0;j<=2;j++)
            {
                System.out.println(arr[i][j]);
            }
        }
    }
}
